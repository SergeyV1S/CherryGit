/**
 * Системные роли пользователя CherryGit (ВКР раздел 2.2.7).
 *
 * DEVELOPER — разработчик; видит свои метрики + командный baseline.
 * LEAD      — тимлид; видит командные данные команд, для которых назначен лидом
 *             (определяется через team_members.role).
 * HEAD      — руководитель отдела; видит кросс-командные DORA в своём отделе.
 * ADMIN     — администратор; управляет конфигурацией, не имеет доступа к значениям метрик.
 */
export type RoleType = 'ADMIN' | 'DEVELOPER' | 'HEAD' | 'LEAD';
